package Model;

public class Ejercicio {

	private int  id_ejercicio;
	private String nombre;
	private int serie;
	private int descanso;
	private int repeteticiones;
	private int puntos_progre_ejerci;
	public int getId_ejercicio() {
		return id_ejercicio;
	}
	public void setId_ejercicio(int id_ejercicio) {
		this.id_ejercicio = id_ejercicio;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public int getSerie() {
		return serie;
	}
	public void setSerie(int serie) {
		this.serie = serie;
	}
	public int getDescanso() {
		return descanso;
	}
	public void setDescanso(int descanso) {
		this.descanso = descanso;
	}
	public int getRepeteticiones() {
		return repeteticiones;
	}
	public void setRepeteticiones(int repeteticiones) {
		this.repeteticiones = repeteticiones;
	}
	public int getPuntos_progre_ejerci() {
		return puntos_progre_ejerci;
	}
	public void setPuntos_progre_ejerci(int puntos_progre_ejerci) {
		this.puntos_progre_ejerci = puntos_progre_ejerci;
	}
	
	
	
}
	
	
	
